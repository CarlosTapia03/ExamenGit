
public class Arbol {

	private String nombre;
	private int altura;
	
	public Arbol(String nombre, int altura) {
		super();
		this.nombre = nombre;
		this.altura = altura;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getAltura() {
		return altura;
	}

	public void setAltura(int altura) {
		this.altura = altura;
	}
	
	
	
}
