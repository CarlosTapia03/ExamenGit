
public class Carlos {

	private String color;
	private int edad;
	
	public Carlos(String color, int edad) {
		super();
		this.color = color;
		this.edad = edad;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	
	
}
